#include <stdio.h>

int	main(void)
{
	int i = 0;
	int k = 6;
	int j = 5;
	while (j > 0)
	{
		while(i < (k- 1))
		{
			printf("*");
			i++;
		}
		i = 0;
		k = k - 1;
		j--;
		printf("\n");
	}
}